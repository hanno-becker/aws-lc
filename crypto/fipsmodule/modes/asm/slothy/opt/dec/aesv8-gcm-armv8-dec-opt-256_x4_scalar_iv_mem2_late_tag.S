.align        4
_aes_gcm_dec_kernel_slothy_base_256:
aes_gcm_dec_kernel_slothy_base_256:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_dec_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Ldec_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

Ldec_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                      // Instructions:    161
                                                      // Expected cycles: 94
                                                      // Expected IPC:    1.71
                                                      //
                                                      // Cycle bound:     94.0
                                                      // IPC bound:       1.71
                                                      //
                                                      // Wall time:     25.19s
                                                      // User time:     25.19s
                                                      //
                                                      // ------------------------------------- cycle (expected) -------------------------------------->
                                                      // 0                        25                       50                       75
                                                      // |------------------------|------------------------|------------------------|------------------
        ldr q31, [x0, #48]                            // *.............................................................................................
        add w8, w13, #2                               // *.............................................................................................
        add w29, w13, #0                              // *.............................................................................................
        add w12, w13, #3                              // *.............................................................................................
        ldr q14, [x6, #80]                            // .*............................................................................................
        add w7, w13, #1                               // .*............................................................................................
        add w13, w13, #UNROLL                         // .*............................................................................................
        rev w26, w12                                  // .*............................................................................................
        ldr q11, [x6]                                 // ..*...........................................................................................
        str w26, [sp, #STACK_BASE_AES_ST + 60]        // ..*........................................................................................... // @slothy:writes=stack_3
        rev w17, w8                                   // ..*...........................................................................................
