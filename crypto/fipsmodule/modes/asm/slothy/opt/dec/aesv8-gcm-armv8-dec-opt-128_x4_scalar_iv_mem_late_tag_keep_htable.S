.align        4
_aes_gcm_dec_kernel_slothy_base_128:
aes_gcm_dec_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_dec_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Ldec_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Ldec_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    141
                                                           // Expected cycles: 66
                                                           // Expected IPC:    2.14
                                                           //
                                                           // Cycle bound:     66.0
                                                           // IPC bound:       2.14
                                                           //
                                                           // Wall time:     110.52s
                                                           // User time:     110.52s
                                                           //
                                                           // ----------------------- cycle (expected) ------------------------>
                                                           // 0                        25                       50
                                                           // |------------------------|------------------------|---------------
        ldr q11, [x0, #16]                                 // *.................................................................
        ldr q10, [x0, #32]                                 // *.................................................................
        add w24, w13, #3                                   // *.................................................................
        add w29, w13, #2                                   // *.................................................................
        ldr q5, [x0, #48]                                  // .*................................................................
        rev w10, w29                                       // .*................................................................
        add w8, w13, #1                                    // .*................................................................
        add w28, w13, #0                                   // .*................................................................
        rev w30, w28                                       // ..*...............................................................
        orr x29, x12, x10, lsl #32                         // ..*...............................................................
        rev w19, w8                                        // ..*...............................................................
        orr x19, x12, x19, lsl #32                         // ...*..............................................................
        stp x11, x29, [sp, #STACK_BASE_AES_ST + 32]        // ...*.............................................................. // @slothy:writes=stack_2
        add w25, w13, #UNROLL                              // ...*..............................................................
        rev64 v31.16B, v10.16B                             // ....*.............................................................
        rev64 v4.16B, v11.16B                              // ....*.............................................................
        add w22, w25, #0                                   // ....*.............................................................
        add w29, w25, #2                                   // ....*.............................................................
        ldr q1, [sp, #STACK_BASE_AES_ST + 32]              // .....*............................................................ // @slothy:reads=stack_2
        rev64 v6.16B, v5.16B                               // .....*............................................................
        stp x11, x19, [sp, #STACK_BASE_AES_ST + 16]        // .....*............................................................ // @slothy:writes=stack_1
        rev w28, w29                                       // .....*............................................................
        pmull2 v3.1q, v4.2d, v15.2d                        // ......*...........................................................
        ext v29.16B, v31.16B, v31.16B, #8                  // ......*...........................................................
        orr x19, x12, x28, lsl #32                         // ......*...........................................................
        add w17, w25, #1                                   // ......*...........................................................
        ldr q0, [sp, #STACK_BASE_AES_ST + 16]              // .......*.......................................................... // @slothy:reads=stack_1
        pmull2 v2.1q, v31.2d, v13.2d                       // .......*..........................................................
