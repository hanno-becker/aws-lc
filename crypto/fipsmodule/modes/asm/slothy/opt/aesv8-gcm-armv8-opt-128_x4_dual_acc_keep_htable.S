.macro ghash_init_pair inputa, inputb, Ha, Hb, Hk_mid
        rev64 \inputa\().16b, \inputa\().16b
        rev64 \inputb\().16b, \inputb\().16b
        eor   \inputa\().16b, \inputa\().16b, tag.16b

