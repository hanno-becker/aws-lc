.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    165
                                                           // Expected cycles: 81
                                                           // Expected IPC:    2.04
                                                           // ----------------------------- expected cycle count ----------------------------->
                                                           // 0                        25                       50                       75
                                                           // |------------------------|------------------------|------------------------|-----
        ldr q10, [x6, #16]                                 // *................................................................................
        add w28, w13, #3                                   // *................................................................................
        add w29, w13, #1                                   // *................................................................................
        add w8, w13, #0                                    // *................................................................................
        ldr q31, [x6, #32]                                 // .*...............................................................................
        add w10, w13, #2                                   // .*...............................................................................
        add w13, w13, #UNROLL                              // .*...............................................................................
        rev w28, w28                                       // .*...............................................................................
        ldr q28, [x6]                                      // ..*..............................................................................
        rev w29, w29                                       // ..*..............................................................................
        rev w10, w10                                       // ..*..............................................................................
        orr x28, x12, x28, lsl #32                         // ..*..............................................................................
        ldr q29, [x6, #64]                                 // ...*.............................................................................
        orr x29, x12, x29, lsl #32                         // ...*.............................................................................
        orr x10, x12, x10, lsl #32                         // ...*.............................................................................
        stp x11, x28, [sp, #STACK_BASE_AES_ST + 48]        // ...*............................................................................. // @slothy:writes=stack_3
        rev w28, w8                                        // ....*............................................................................
        stp x11, x10, [sp, #STACK_BASE_AES_ST + 32]        // ....*............................................................................ // @slothy:writes=stack_2
        add w8, w13, #1                                    // ....*............................................................................
        ldr q9, [sp, #STACK_BASE_AES_ST + 48]              // .....*........................................................................... // @slothy:reads=stack_3
        orr x28, x12, x28, lsl #32                         // .....*...........................................................................
        rev w8, w8                                         // .....*...........................................................................
        add w17, w13, #0                                   // .....*...........................................................................
        ldr q2, [sp, #STACK_BASE_AES_ST + 32]              // ......*.......................................................................... // @slothy:reads=stack_2
