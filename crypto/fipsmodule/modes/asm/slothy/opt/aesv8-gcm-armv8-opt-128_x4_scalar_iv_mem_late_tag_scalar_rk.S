.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    148
                                                           // Expected cycles: 70
                                                           // Expected IPC:    2.11
                                                           //
                                                           // Cycle bound:     70.0
                                                           // IPC bound:       2.11
                                                           //
                                                           // Wall time:     6.22s
                                                           // User time:     6.22s
                                                           //
                                                           // ------------------------- cycle (expected) -------------------------->
                                                           // 0                        25                       50
                                                           // |------------------------|------------------------|-------------------
        ldr q8, [x6]                                       // *.....................................................................
        add w17, w13, #1                                   // *.....................................................................
        add w10, w13, #0                                   // *.....................................................................
        add w23, w13, #3                                   // *.....................................................................
        ldr q11, [x6, #80]                                 // .*....................................................................
        rev w24, w10                                       // .*....................................................................
        rev w30, w17                                       // .*....................................................................
        rev w19, w23                                       // .*....................................................................
        ldr q6, [x6, #32]                                  // ..*...................................................................
        orr x25, x12, x30, lsl #32                         // ..*...................................................................
        orr x26, x12, x24, lsl #32                         // ..*...................................................................
        orr x23, x12, x19, lsl #32                         // ..*...................................................................
        add w27, w13, #2                                   // ...*..................................................................
        add w13, w13, #UNROLL                              // ...*..................................................................
        stp x11, x23, [sp, #STACK_BASE_AES_ST + 48]        // ...*.................................................................. // @slothy:writes=stack_3
        add w7, w13, #2                                    // ....*.................................................................
        add w29, w13, #0                                   // ....*.................................................................
        stp x11, x26, [sp, #STACK_BASE_AES_ST]             // ....*................................................................. // @slothy:writes=stack_0
        ldr q15, [sp, #STACK_BASE_AES_ST + 48]             // .....*................................................................ // @slothy:reads=stack_3
        rev w26, w7                                        // .....*................................................................
        rev w10, w27                                       // .....*................................................................
        ldr q9, [sp, #STACK_BASE_AES_ST]                   // ......*............................................................... // @slothy:reads=stack_0
