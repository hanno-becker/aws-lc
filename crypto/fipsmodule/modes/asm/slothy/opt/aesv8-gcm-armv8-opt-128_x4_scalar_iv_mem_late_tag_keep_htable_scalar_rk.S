.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    108
                                                           // Expected cycles: 57
                                                           // Expected IPC:    1.89
                                                           // ----------------- expected cycle count ----------------->
                                                           // 0                        25                       50
                                                           // |------------------------|------------------------|------
        add w14, w13, #3                                   // *........................................................
        rev w29, w14                                       // .*.......................................................
        orr x14, x12, x29, lsl #32                         // ..*......................................................
        stp x11, x14, [sp, #STACK_BASE_AES_ST + 48]        // ...*..................................................... // @slothy:writes=stack_3
        add w30, w13, #0                                   // ....*....................................................
        rev w8, w30                                        // .....*...................................................
        ldr q11, [sp, #STACK_BASE_AES_ST + 48]             // ......*.................................................. // @slothy:reads=stack_3
        orr x28, x12, x8, lsl #32                          // .......*.................................................
        stp x11, x28, [sp, #STACK_BASE_AES_ST]             // ........*................................................ // @slothy:writes=stack_0
        aesr v11.16b, v18.16b                              // .........*...............................................
        add w25, w13, #1                                   // .........*...............................................
        add w19, w13, #2                                   // ..........*..............................................
        aesr v11.16b, v19.16b                              // ...........*.............................................
        ldr q2, [sp, #STACK_BASE_AES_ST]                   // ...........*............................................. // @slothy:reads=stack_0
        add w13, w13, #UNROLL                              // ............*............................................
        aesr v11.16b, v20.16b                              // .............*...........................................
        rev w26, w19                                       // .............*...........................................
        aesr v2.16b, v18.16b                               // ..............*..........................................
        add w14, w13, #2                                   // ..............*..........................................
        aesr v11.16b, v21.16b                              // ...............*.........................................
        orr x19, x12, x26, lsl #32                         // ...............*.........................................
        aesr v2.16b, v19.16b                               // ................*........................................
