.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    141
                                                           // Expected cycles: 69
                                                           // Expected IPC:    2.04
                                                           // ----------------------- expected cycle count ----------------------->
                                                           // 0                        25                       50
                                                           // |------------------------|------------------------|------------------
        add w25, w13, #UNROLL                              // *....................................................................
        ldp x14, x24, [x0, #32]                            // *....................................................................
        add w22, w13, #0                                   // *....................................................................
        add w8, w25, #1                                    // .*...................................................................
        rev w22, w22                                       // .*...................................................................
        add w7, w13, #1                                    // .*...................................................................
        orr x26, x12, x22, lsl #32                         // ..*..................................................................
        add w19, w25, #0                                   // ..*..................................................................
        rev w8, w8                                         // ..*..................................................................
        orr x22, x12, x8, lsl #32                          // ...*.................................................................
        stp x11, x26, [sp, #STACK_BASE_AES_ST]             // ...*................................................................. // @slothy:writes=stack_0
        rev w26, w19                                       // ...*.................................................................
        orr x23, x12, x26, lsl #32                         // ....*................................................................
        stp x11, x22, [sp, #STACK_BASE_AES_ST + 16]        // ....*................................................................ // @slothy:writes=stack_1
        rev w27, w7                                        // ....*................................................................
        ldr q0, [sp, #STACK_BASE_AES_ST]                   // .....*............................................................... // @slothy:reads=stack_0
        add w17, w13, #3                                   // .....*...............................................................
        add w28, w13, #2                                   // .....*...............................................................
        orr x10, x12, x27, lsl #32                         // .....*...............................................................
        ldr q28, [sp, #STACK_BASE_AES_ST + 16]             // ......*.............................................................. // @slothy:reads=stack_1
        stp x11, x10, [sp, #STACK_BASE_AES_ST + 16]        // ......*.............................................................. // @slothy:writes=stack_1
        add w7, w25, #3                                    // ......*..............................................................
        rev w22, w28                                       // ......*..............................................................
