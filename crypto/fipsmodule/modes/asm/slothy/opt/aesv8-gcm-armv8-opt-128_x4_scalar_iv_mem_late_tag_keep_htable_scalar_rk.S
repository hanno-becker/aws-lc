.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    185
                                                           // Expected cycles: 85
                                                           // Expected IPC:    2.18
                                                           // ------------------------------- expected cycle count ------------------------------->
                                                           // 0                        25                       50                       75
                                                           // |------------------------|------------------------|------------------------|---------
        add w23, w13, #3                                   // *....................................................................................
        add w14, w13, #UNROLL                              // *....................................................................................
        add w17, w14, #0                                   // .*...................................................................................
        ldp x30, x19, [x0], #(4*16)                        // .*...................................................................................
        rev w28, w23                                       // .*...................................................................................
        rev w22, w17                                       // ..*..................................................................................
        orr x10, x12, x28, lsl #32                         // ..*..................................................................................
        stp x11, x10, [sp, #STACK_BASE_AES_ST + 48]        // ...*................................................................................. // @slothy:writes=stack_3
        orr x8, x12, x22, lsl #32                          // ...*.................................................................................
        add w25, w14, #3                                   // ....*................................................................................
        stp x11, x8, [sp, #STACK_BASE_AES_ST]              // ....*................................................................................ // @slothy:writes=stack_0
        add w23, w14, #2                                   // .....*...............................................................................
        add w10, w13, #0                                   // .....*...............................................................................
        ldr q31, [sp, #STACK_BASE_AES_ST + 48]             // .....*............................................................................... // @slothy:reads=stack_3
        ldr q1, [sp, #STACK_BASE_AES_ST]                   // ......*.............................................................................. // @slothy:reads=stack_0
        eor x28, x30, x20                                  // ......*..............................................................................
        add w8, w13, #1                                    // ......*..............................................................................
        ldp x26, x27, [x0], #(4*16)                        // ......*..............................................................................
        eor x17, x19, x21                                  // .......*.............................................................................
        rev w19, w10                                       // .......*.............................................................................
        stp x28, x17, [sp, #STACK_BASE_AES_ST]             // ........*............................................................................ // @slothy:writes=stack_0
        rev w30, w8                                        // ........*............................................................................
