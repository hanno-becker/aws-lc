.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    171
                                                           // Expected cycles: 81
                                                           // Expected IPC:    2.11
                                                           // ----------------------------- expected cycle count ----------------------------->
                                                           // 0                        25                       50                       75
                                                           // |------------------------|------------------------|------------------------|-----
        add w17, w13, #2                                   // *................................................................................
        add w27, w13, #3                                   // *................................................................................
        add w23, w13, #0                                   // *................................................................................
        rev w24, w27                                       // .*...............................................................................
        rev w7, w23                                        // .*...............................................................................
        rev w8, w17                                        // .*...............................................................................
        orr x14, x12, x8, lsl #32                          // ..*..............................................................................
        add w26, w13, #1                                   // ..*..............................................................................
        orr x30, x12, x24, lsl #32                         // ..*..............................................................................
        stp x11, x14, [sp, #STACK_BASE_AES_ST + 32]        // ...*............................................................................. // @slothy:writes=stack_2
        add w13, w13, #UNROLL                              // ...*.............................................................................
        stp x11, x30, [sp, #STACK_BASE_AES_ST + 48]        // ...*............................................................................. // @slothy:writes=stack_3
        orr x10, x12, x7, lsl #32                          // ....*............................................................................
        add w19, w13, #1                                   // ....*............................................................................
        add w28, w13, #3                                   // ....*............................................................................
        ldr q6, [sp, #STACK_BASE_AES_ST + 32]              // .....*........................................................................... // @slothy:reads=stack_2
        stp x11, x10, [sp, #STACK_BASE_AES_ST]             // .....*........................................................................... // @slothy:writes=stack_0
        rev w22, w26                                       // .....*...........................................................................
        rev w29, w19                                       // .....*...........................................................................
        ldr q1, [sp, #STACK_BASE_AES_ST + 48]              // ......*.......................................................................... // @slothy:reads=stack_3
        rev w28, w28                                       // ......*..........................................................................
        orr x29, x12, x29, lsl #32                         // ......*..........................................................................
