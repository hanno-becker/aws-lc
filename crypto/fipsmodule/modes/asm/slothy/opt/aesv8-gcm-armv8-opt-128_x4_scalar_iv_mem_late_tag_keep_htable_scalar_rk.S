.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    64
                                                           // Expected cycles: 32
                                                           // Expected IPC:    2.00
                                                           //
                                                           // Cycle bound:     32.0
                                                           // IPC bound:       2.00
                                                           //
                                                           // Wall time:     0.68s
                                                           // User time:     0.68s
                                                           //
                                                           // ------ cycle (expected) ------->
                                                           // 0                        25
                                                           // |------------------------|------
        add w26, w13, #3                                   // *...............................
        add w30, w13, #2                                   // *...............................
        add w25, w13, #0                                   // *...............................
        rev w17, w30                                       // .*..............................
        rev w8, w25                                        // .*..............................
        ldp x23, x30, [x0, #32]                            // .*..............................
        rev w29, w26                                       // ..*.............................
        orr x19, x12, x8, lsl #32                          // ..*.............................
        add w24, w13, #1                                   // ..*.............................
        rev w22, w24                                       // ...*............................
        stp x11, x19, [sp, #STACK_BASE_AES_ST]             // ...*............................ // @slothy:writes=stack_0
        ldp x8, x26, [x0, #48]                             // ...*............................
        orr x7, x12, x29, lsl #32                          // ....*...........................
        orr x14, x12, x22, lsl #32                         // ....*...........................
        ldr q4, [sp, #STACK_BASE_AES_ST]                   // .....*.......................... // @slothy:reads=stack_0
        stp x11, x14, [sp, #STACK_BASE_AES_ST + 16]        // .....*.......................... // @slothy:writes=stack_1
        eor x27, x23, x20                                  // ......*.........................
        stp x11, x7, [sp, #STACK_BASE_AES_ST + 48]         // ......*......................... // @slothy:writes=stack_3
        ldr q3, [sp, #STACK_BASE_AES_ST + 16]              // .......*........................ // @slothy:reads=stack_1
        eor x10, x8, x20                                   // .......*........................
