.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    156
                                                           // Expected cycles: 70
                                                           // Expected IPC:    2.23
                                                           // ----------------------- expected cycle count ------------------------>
                                                           // 0                        25                       50
                                                           // |------------------------|------------------------|-------------------
        ldp x10, x28, [x0, #32]                            // *.....................................................................
        ldp x25, x22, [x0, #112]                           // *.....................................................................
        add w7, w13, #0                                    // *.....................................................................
        add w30, w13, #2                                   // .*....................................................................
        add w17, w13, #UNROLL                              // .*....................................................................
        rev w14, w7                                        // .*....................................................................
        ldp x27, x26, [x0, #96]                            // ..*...................................................................
        orr x8, x12, x14, lsl #32                          // ..*...................................................................
        rev w7, w30                                        // ..*...................................................................
        eor x10, x10, x20                                  // ...*..................................................................
        stp x11, x8, [sp, #STACK_BASE_AES_ST]              // ...*.................................................................. // @slothy:writes=stack_0
        orr x24, x12, x7, lsl #32                          // ...*..................................................................
        add w19, w13, #3                                   // ....*.................................................................
        add w8, w13, #1                                    // ....*.................................................................
        stp x11, x24, [sp, #STACK_BASE_AES_ST + 32]        // ....*................................................................. // @slothy:writes=stack_2
        rev w8, w8                                         // .....*................................................................
        rev w19, w19                                       // .....*................................................................
        ldr q29, [sp, #STACK_BASE_AES_ST]                  // ......*............................................................... // @slothy:reads=stack_0
        eor x24, x28, x21                                  // ......*...............................................................
        orr x19, x12, x19, lsl #32                         // ......*...............................................................
        add w14, w17, #2                                   // ......*...............................................................
        ldr q9, [sp, #STACK_BASE_AES_ST + 32]              // .......*.............................................................. // @slothy:reads=stack_2
        rev w29, w14                                       // .......*..............................................................
