.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                           // Instructions:    128
                                                           // Expected cycles: 63
                                                           // Expected IPC:    2.03
                                                           //
                                                           // Cycle bound:     63.0
                                                           // IPC bound:       2.03
                                                           //
                                                           // Wall time:     4.10s
                                                           // User time:     4.10s
                                                           //
                                                           // ---------------------- cycle (expected) ---------------------->
                                                           // 0                        25                       50
                                                           // |------------------------|------------------------|------------
        ldr q15, [x0, #32]                                 // *..............................................................
        add w14, w13, #0                                   // *..............................................................
        add w29, w13, #2                                   // *..............................................................
        add w10, w13, #1                                   // *..............................................................
        ldr q29, [x6, #48]                                 // .*.............................................................
        rev w29, w29                                       // .*.............................................................
        add w23, w13, #UNROLL                              // .*.............................................................
        rev w24, w10                                       // .*.............................................................
        ldr q0, [x0, #48]                                  // ..*............................................................
        orr x21, x12, x29, lsl #32                         // ..*............................................................
        orr x29, x12, x24, lsl #32                         // ..*............................................................
        add w26, w23, #1                                   // ..*............................................................
        ldr q31, [x6, #80]                                 // ...*...........................................................
        stp x11, x21, [sp, #STACK_BASE_AES_ST + 32]        // ...*........................................................... // @slothy:writes=stack_2
        add w24, w23, #2                                   // ...*...........................................................
        eor v10.16B, v15.16B, v28.16B                      // ....*..........................................................
        rev w21, w24                                       // ....*..........................................................
        stp x11, x29, [sp, #STACK_BASE_AES_ST + 16]        // ....*.......................................................... // @slothy:writes=stack_1
        rev w14, w14                                       // ....*..........................................................
        ldr q6, [sp, #STACK_BASE_AES_ST + 32]              // .....*......................................................... // @slothy:reads=stack_2
        add w27, w13, #3                                   // .....*.........................................................
        orr x24, x12, x21, lsl #32                         // .....*.........................................................
        add w13, w23, #UNROLL                              // .....*.........................................................
