.align        4
_aes_gcm_enc_kernel_slothy_base_128:
aes_gcm_enc_kernel_slothy_base_128:
#ifdef BORINGSSL_DISPATCH_TEST
        adrp  x9,_BORINGSSL_function_hit@PAGE
        add   x9, x9, _BORINGSSL_function_hit@PAGEOFF
        mov   w10, #1
        strb  w10, [x9,#2] // kFlag_aes_gcm_enc_kernel
#endif

        AARCH64_VALID_CALL_TARGET
        sub sp, sp, #STACK_SIZE

Lenc_preamble_start:
        save_gprs
        save_vregs

        lsr byte_len, len_bits, #3

        load_round_keys
        load_tag
        load_iv

        prepare_loop_counts
        prepare_ghash

        load_htable_12
        load_htable_34

Lenc_preamble_end:

        cbz count, Lloop_unrolled_end
        cmp count, #1
        b.eq Lloop_unrolled_start_iter_1
                                                          // Instructions:    141
                                                          // Expected cycles: 72
                                                          // Expected IPC:    1.96
                                                          //
                                                          // Cycle bound:     72.0
                                                          // IPC bound:       1.96
                                                          //
                                                          // Wall time:     2.22s
                                                          // User time:     2.22s
                                                          //
                                                          // -------------------------- cycle (expected) --------------------------->
                                                          // 0                        25                       50
                                                          // |------------------------|------------------------|---------------------
        add w23, w13, #2                                  // *.......................................................................
        add w25, w13, #3                                  // *.......................................................................
        add w10, w13, #1                                  // *.......................................................................
        add w7, w13, #0                                   // .*......................................................................
        add w14, w13, #UNROLL                             // .*......................................................................
        rev w23, w23                                      // .*......................................................................
        rev w25, w25                                      // ..*.....................................................................
        rev w10, w10                                      // ..*.....................................................................
        str w23, [sp, #STACK_BASE_AES_ST + 44]            // ..*..................................................................... // @slothy:writes=stack_2
        str w25, [sp, #STACK_BASE_AES_ST + 60]            // ...*.................................................................... // @slothy:writes=stack_3
        str w10, [sp, #STACK_BASE_AES_ST + 28]            // ...*.................................................................... // @slothy:writes=stack_1
        add w23, w14, #2                                  // ...*....................................................................
        ldr q3, [sp, #STACK_BASE_AES_ST + 32]             // ....*................................................................... // @slothy:reads=stack_2
