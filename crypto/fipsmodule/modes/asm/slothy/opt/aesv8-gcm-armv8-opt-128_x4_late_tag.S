        ldr q8, [x6, #32]                        // ........*...........................................
        aesr v14.16b, v20.16b                    // .........*..........................................
        add v12.4S, v12.4S, v30.4S               // .........*..........................................
        aesr v16.16b, v22.16b                    // ..........*.........................................
        aesr v14.16b, v21.16b                    // ...........*........................................
        add v9.4S, v12.4S, v30.4S                // ...........*........................................
        aesr v16.16b, v23.16b                    // ............*.......................................
        rev32 v31.16B, v12.16B                   // ............*.......................................
        aesr v14.16b, v22.16b                    // .............*......................................
        rev32 v10.16B, v9.16B                    // .............*......................................
        aesr v16.16b, v24.16b                    // ..............*.....................................
        ldr q12, [x0, #-16]                      // ..............*.....................................
        aesr v10.16b, v18.16b                    // ...............*....................................
        aesr v16.16b, v25.16b                    // ................*...................................
        aesr v10.16b, v19.16b                    // .................*..................................
        aesr v16.16b, v26.16b                    // ..................*.................................
        aesr v10.16b, v20.16b                    // ...................*................................
        aese v16.16b, v27.16b                    // ....................*...............................
