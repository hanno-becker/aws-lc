        rev32 v14.16B, v12.16B                   // ...*................................................
        aesr v16.16b, v19.16b                    // ....*...............................................
        eor v2.16B, v0.16B, v28.16B              // .....*..............................................
        aesr v14.16b, v18.16b                    // .....*..............................................
        aesr v16.16b, v20.16b                    // ......*.............................................
        aesr v14.16b, v19.16b                    // .......*............................................
