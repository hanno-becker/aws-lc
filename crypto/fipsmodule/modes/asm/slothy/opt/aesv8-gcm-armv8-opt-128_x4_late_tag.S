        // Expected cycles: 52
        // Expected IPC:    1.7307692307692308
        add v12.4S, v31.4S, v30.4S               // *...................................................
        rev32 v16.16B, v31.16B                   // *...................................................
        ldr q13, [x6, #48]                       // .*..................................................
        ldr q29, [x0, #16]                       // .*..................................................
        ldr q0, [x0], #(4*16)                    // ..*.................................................
